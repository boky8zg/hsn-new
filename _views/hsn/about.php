    <div class="main about">
        <hr style="border-color: #d9aa49" />
        <p>
            Hrvatska sveučilišna naklada d.o.o., tvrtka je u vlasništvu Sveučilišta u 
            Zagrebu. Osnovana je 23. siječnja 1992. godine odlukom Predsjedništva 
            Zajednice sveučilišta s temeljnim ciljem da bude nakladnički servis svih 
            hrvatskih sveučilišta, znanstvenih, obrazovnih i kulturnih institucija te 
            pojedinaca u Republici Hrvatskoj.
        </p>
        <p>
            Nastojanja da se tiskaju i nekomercijalna djela visoke vrijednosti, koja 
            predstavljaju temelje naše civilizacije i kulture, pružio je Hrvatskoj sveučilišnoj 
            nakladi legitimaciju među izdavačima, znanstvenicima i kulturnim djelatnicima, 
            te je Hrvatska sveučilišna naklada postala omiljena nakladnička adresa sa 
            golemom mrežom autora i vanjskih suradnika. U dvadeset četiri godine rada 
            objavljene su stotine knjiga u kojima kriteriji za objavljivanje nije isključivo 
            komercijalan.
        </p>
        <p>
            Nakladnička djelatnost prati i aktualna društvena zbivanja. Nakladnički projekt 
            Croatia in the war (1992.) na stanovit je način naslijedio projekt Svjedoci 
            povijesti (1997) u kojem posredni ili neposredni sudionici svjedoče o 
            zbivanjima vezanim s raspadom bivše Jugoslavije.
            Među ostalim naslovima osobitu su pozornost u javnosti privukle knjige I. 
            Rogića Tko je Zagreb, B. Perasovića Urbana plemena, A. Šoljana Prošlo 
            nesvršeno vrijeme, J. Laušića Sutoka, T. Maroevića Klik, G. Sušca Jutarnja 
            novost, I. Boškovića Orjuna-Književnost i ideologija te Kulturni bestijarij koje 
            su nagrađene prestižnim nagradama i državnim nagradama za znanost.
            Poseban status ima novopokrenuta biblioteka FORENZIKA u kojoj planiramo 
            pokriti široko polje forenzične znanosti.
            Ovogodišnji broj objavljenih naslova je trideset, što Hrvatsku sveučilišnu 
            nakladu svrstava među srednje hrvatske nakladnike. 
            Zalažemo se za napredak nakladničke djelatnosti koja bi, osim njegovanja i 
            očuvanja hrvatske znanstvene i kulturne baštine, pred čitatelje donijela knjige 
            svjetske kulture i znanosti s osobitim naglaskom na potrebe hrvatskih 
            sveučilišta (znanstvena literatura i udžbenici). 
            Za našu nakladničku orijentaciju karakteristično je više znanstvenih izdanja 
            koja su korisna i zanimljiva široj čitalačkoj publici čime nastojimo popularizirati 
            najnovije znanstvene spoznaje i dosege hrvatske akademske zajednice.
        </p>
